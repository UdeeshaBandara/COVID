package com.MAD.healthapp;

public class Users {

    String Name;
    String Image;
    String Status;


    public Users() {

    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getImage() {
        return Image;
    }

    public void setImage(String image) {
        Image = image;
    }

    public String getStatus() {
        return Status;
    }


    public void setStatus(String status) {
        Status = status;
    }
}
